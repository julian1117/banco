package co.edu.eam.ingesoft.pa.banco.web.servicios.dto;

public class CuentaAsociadaDTO {
	
	private String idBanco;
	
	private String nombreAso;
	
	private String numero;

	public CuentaAsociadaDTO() {
		super();
	}

	public String getIdBanco() {
		return idBanco;
	}

	public void setIdBanco(String idBanco) {
		this.idBanco = idBanco;
	}

	public String getNombreAso() {
		return nombreAso;
	}

	public void setNombreAso(String nombreAso) {
		this.nombreAso = nombreAso;
	}

	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}

	
	

}
